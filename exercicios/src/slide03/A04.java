package slide03;

public class A04 {
	private Double n1, n2;

	public Double soma() {
		return n1 + n2;
	}

	public Double sub() {
		return n1 - n2;
	}

	public Double mul() {
		return n1 * n2;
	}

	public Double div() {
		return n1 / n2;
	}

	public Double getN1() {
		return n1;
	}

	public void setN1(Double n1) {
		this.n1 = n1;
	}

	public Double getN2() {
		return n2;
	}

	public void setN2(Double n2) {
		this.n2 = n2;
	}

}
